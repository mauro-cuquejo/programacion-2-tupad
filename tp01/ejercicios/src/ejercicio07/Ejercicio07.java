package ejercicio07;

public class Ejercicio07 {

    public static void main(String[] args) {
        int x = 10; // Línea 1 la instruccion es int x = 10, que sería almacenar el valor 10 en x. La expresión es numérica, y sería 10.
        x = x + 5; // Línea 2 la instruccion es x = x + 5, en donde se le asigna a x la suma del valor actual de x, más cinco. La expresión sería x + 5
        System.out.println(x); // Línea 3 la instrucción es System.out.println(x), en donde se muestra por consola el contenido de la variable x. La expresión, sería x, que en este caso es numérica.
    }
}
